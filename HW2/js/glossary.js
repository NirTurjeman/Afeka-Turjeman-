const glossary = document.querySelector("#glossary");
const search = document.querySelector("#searchTerm");
const count = document.querySelector("#termCount");

function render() {
  const query = search.value.trim().toLowerCase();
  const filtered = terms.filter(([name, text]) =>
    `${name} ${text}`.toLowerCase().includes(query)
  );
  glossary.innerHTML = filtered.map(([name, text]) => `
    <article class="term-card">
      <h3>${name}</h3>
      <p>${text}</p>
    </article>
  `).join("");
  count.textContent = `${filtered.length} מושגים`;
}

search.addEventListener("input", render);
render();
